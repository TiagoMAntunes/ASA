#include "list.h"
#include <stdlib.h>
#include <stdio.h>

/*******************************************************************************
 * create_node
 *******************************************************************************
*/
Node create_node(Item i) {
    Node n = malloc(sizeof(struct node));
    n->item = i;
    n->next = NULL;
    
    return n;
}


/*******************************************************************************
 * free_node
 *******************************************************************************
*/
void free_node(Node n) {
    delete_item(n->item);
    free(n);
}

/*******************************************************************************
 * insert
 *******************************************************************************
*/
void insert(Node head, Node new) {
    new->next = head->next;
    head->next = new;
}

/*******************************************************************************
 * free_all
 *******************************************************************************
*/
void free_all(Node head) {
    for (Node h = head, aux = h; h != NULL; aux = h) {
        h = h->next;
        free_node(aux);
    }
}

/*******************************************************************************
 * next
 *******************************************************************************
*/
Node next(Node h) {
    return h->next;
}